#include "libft.h"

int		ft_check_end(char *s1)
{
	int		i;

	i = 0;
	if (!s1)
		return (-1);
	while (s1[i])
	{
		if (s1[i] == '\n')
			return (i);
		i++;
	}
	return (-1);
}
