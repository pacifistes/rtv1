#include "libft.h"

int		ft_nbr_case(char *s1, char c)
{
	int i;
	int nbr;

	i = 0;
	nbr = 0;
	while (s1[i] != '\0' && s1[i] != c)
	{
		if (s1[i] >= '0' && s1[i] <= '9' && (s1[i + 1] == ' '
		|| s1[i + 1] == c || s1[i + 1] == ','))
			nbr++;
		i++;
	}
	return (nbr);
}
